
package tp3.bucles;

public class Tp3Bucles {

    
    public static void main(String[] args) {
  
        
    
        for (int a = 1; a <= 100; a++) {
            System.out.println(a);
        }

        int b = 1;
        while (b <= 100) {
            System.out.println(b);
            b++;
        }

        int c = 1;
        do {
            System.out.println(c);
            c++;
        } while (c <= 100);
    }
}

    

